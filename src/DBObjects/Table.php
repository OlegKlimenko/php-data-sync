<?php
//----------------------------------------------------------------------------------------------------------------------
namespace SetBased\DataSync\DBObjects;

//----------------------------------------------------------------------------------------------------------------------
/**
 * Class for tables.
 */
class Table
{
  // -------------------------------------------------------------------------------------------------------------------
  /**
   * The name of a table.
   *
   * @var string
   */
  public $tableName;

  /**
   * The list of records.
   *
   * @var array
   */
  public $records;
  
  //--------------------------------------------------------------------------------------------------------------------
  /**
   * Object constructor.
   *
   * @param string $tableName The name of a table.
   * @param array  $records   The list of records.
   */
  public function __construct($tableName, $records)
  {
    $this->tableName = $tableName;

    foreach($records as $record_num => $record)
    {
      $this->records[] = new Record($record);
    }
  }
  
  // -------------------------------------------------------------------------------------------------------------------

}

